const deleteOrder = ["ShoppingListItem", "MealPlan", "Wine", "Recipe", "HouseholdMember", "Household", "User"];
const insertOrder = ["User", "Household", "HouseholdMember", "Recipe", "MealPlan", "ShoppingListItem", "Wine"];

if (!SOURCE_URL) {
  console.error("Missing SUPABASE_URL or NEXT_PUBLIC_SUPABASE_URL");
  process.exit(1);
}

if (!SERVICE_KEY) {
  console.error("Missing SUPABASE_SERVICE_ROLE_KEY or SUPABASE_SECRET_KEY");
  process.exit(1);
}

const headers = {
  apikey: SERVICE_KEY,
  Authorization: `Bearer ${SERVICE_KEY}`,
};

const fetchTable = async (table) => {
  const rows = [];
  let offset = 0;

  for (;;) {
    const response = await fetch(
      `${SOURCE_URL}/rest/v1/${encodeURIComponent(table)}?select=*`,
      {
        headers: {
          ...headers,
          Range: `${offset}-${offset + PAGE_SIZE - 1}`,
          Prefer: "count=exact",
        },
      }
    );

    if (!response.ok) {
      const body = await response.text();
      throw new Error(`Failed to fetch ${table}: ${response.status} ${body}`);
    }

    const batch = await response.json();
    rows.push(...batch);

    if (batch.length < PAGE_SIZE) {
      break;
    }

    offset += PAGE_SIZE;
  }

  return rows;
};

const escapeSqlString = (value) => String(value).replace(/'/g, "''");

const toSqlLiteral = (value) => {
  if (value === null || value === undefined) {
    return "NULL";
  }

  if (Array.isArray(value) || (typeof value === "object" && !(value instanceof Date))) {
    return `'${escapeSqlString(JSON.stringify(value))}'`;
  }

  if (typeof value === "boolean") {
    return value ? "1" : "0";
  }

  if (typeof value === "number") {
    return Number.isFinite(value) ? String(value) : "NULL";
  }

  return `'${escapeSqlString(value)}'`;
};

const normalizeRow = (tableName, row) => {
  if (tableName === "User" && !("betterAuthUserId" in row)) {
    return { ...row, betterAuthUserId: null };
  }

  if (tableName === "Wine" && !("stockists" in row)) {
    return { ...row, stockists: null };
  }

  return row;
};

const main = async () => {
  const allData = {};

  for (const table of tables) {
    const rows = await fetchTable(table.name);
    allData[table.name] = rows.map((row) => normalizeRow(table.name, row));
  }

  const statements = [
    "-- Generated by scripts/migrate-supabase-to-d1.mjs",
    "-- D1 remote imports reject explicit BEGIN/COMMIT statements, so this file is generated",
    "-- in a D1-compatible form and should be applied with `wrangler d1 execute --file`.",
    "PRAGMA defer_foreign_keys = on;",
    ...deleteOrder.map((tableName) => `DELETE FROM "${tableName}";`),
  ];

  for (const tableName of insertOrder) {
    const table = tables.find((entry) => entry.name === tableName);
    const rows = allData[tableName];
    if (!table || rows.length === 0) {
      continue;
    }

    for (const row of rows) {
      const columns = table.fields.map((field) => `"${field}"`).join(", ");
      const values = table.fields.map((field) => toSqlLiteral(row[field])).join(", ");
      statements.push(`INSERT INTO "${tableName}" (${columns}) VALUES (${values});`);
    }
  }
  const now = new Date().toISOString().replace(/[:.]/g, "-");
  const outputPath = path.join("backups", `supabase-to-d1-${now}.sql`);
  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, `${statements.join("\n")}\n`, "utf8");

  const counts = Object.fromEntries(
    Object.entries(allData).map(([tableName, rows]) => [tableName, rows.length])
  );

  console.log(JSON.stringify({ outputPath, counts }, null, 2));
};

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
